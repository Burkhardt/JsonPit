		var flag = new MasterFlagFile(dir, "Master", server: "OtherMachine");
		Assert.False(flag.IsExpired);
		Assert.False(flag.TryClaim());
		Assert.Equal("OtherMachine", flag.Originator);
	}
	[Fact]
