	#region MasterFlagFile ticket tests
	[Fact]
	public void IsExpired_TrueForFreshEmptyFlag()
	{
		var dir = (root / "expired-empty").mkdir();
		var flag = new MasterFlagFile(dir, "Master");
		Assert.True(flag.IsExpired);
	}
	[Fact]
