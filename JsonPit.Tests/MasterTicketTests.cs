using System;
using System.IO;
using System.Linq;
using OsLib;
using Xunit;
namespace JsonPit.Tests;
/// <summary>
/// Tests for the timed-ticket master flag protocol and the 6-step MergeChanges workflow.
/// </summary>
public sealed class MasterTicketTests : IDisposable
{
	private readonly RaiPath root;
	private readonly TimeSpan originalDuration;
	public MasterTicketTests()
	{
		originalDuration = MasterFlagFile.TicketDuration;
		root = new RaiPath(Path.GetTempPath()) / "RAIkeep" / "jsonpit-tests" / "master-ticket";
		Cleanup();
		root.mkdir();
	}
	public void Dispose()
	{
		MasterFlagFile.TicketDuration = originalDuration;
		Cleanup();
	}
	private void Cleanup()
	{
		try
		{
			if (root.Exists())
				new RaiFile(root.Path).rmdir(depth: 10, deleteFiles: true);
		}
		catch { }
	}
